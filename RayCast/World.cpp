void walk(float &player_x, float &player_y, float &angle, float time_dif)
{
    if (ImGui::IsKeyPressed(ImGuiKey_W))
    {
        player_x += 580 * std::cos(angle * TO_RADIANTS) * time_dif;
        player_y -= 580 * std::sin(angle * TO_RADIANTS) * time_dif;
        // std::cout << "cos:" << cos(angle * TO_RADIANTS) << '\n';
        // std::cout << "sin:" << sin(angle * TO_RADIANTS) << '\n';
    }
    if (ImGui::IsKeyPressed(ImGuiKey_S))
    {
        player_x -= 580 * std::cos(angle * TO_RADIANTS) * time_dif;
        player_y += 580 * std::sin(angle * TO_RADIANTS) * time_dif;
        // std::cout << "cos:" << cos(angle * TO_RADIANTS) << '\n';
        // std::cout << "sin:" << sin(angle * TO_RADIANTS) << '\n';
    }
}

void rotate(float &angle, float time_dif)
{
    if (ImGui::IsKeyPressed(ImGuiKey_A))
        angle += 360 * time_dif;
    if (ImGui::IsKeyPressed(ImGuiKey_D))
        angle -= 360 * time_dif;
    if (angle > 180.0f)
        angle -= 360.0f;
    else if (angle < -180.0f)
        angle += 360.0f;
}
